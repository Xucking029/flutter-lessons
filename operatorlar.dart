void main(List<String> args) {
  int a = 155, b = 7;
  print(a ~/ b); // { ~/ } bunda natijani consolga chiqarganda faqat butun qismini chiqarib beradi
  print(a / b);  // { / } bunda natijani consolga chiqarganda o'nli kasr ko'rinishida chiqarib beradi
  print(a % b);  // { % } bunda natijani consolga chiqarganda faqat qoldiq qismini chiqarib beradi
}


